package fundamentos;

public class areacircunferencia {
    public static void main(String[] args){
        double raio = 3.4;
        //Definição de constante
        final double PI = 3.14159;
        double area = PI * raio * raio;

        System.out.println("Area = " + area);
        System.out.println(area);
    }
}
